

public class Piste 
{
   private String code ;
   private Vol vol ;
   public Piste (String code , Vol vol) 
   {
       
        this.code = code;
        this.vol = vol;
   }   

    public String getCode() {
        return code;
    }

    public Vol getVol() {
        return vol;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public void setVol(Vol vol) {
        this.vol = vol;
    }
    
    
    
    
    
    
    
}
